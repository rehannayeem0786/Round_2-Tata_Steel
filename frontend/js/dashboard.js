/**
 * Handle real-time sensor updates.
 */
function handleSensorUpdate(data) {
    if (!Array.isArray(data)) return;

    // Live-update the Digital Twin node overlays with the latest readings.
    updateTwinSensors(data);

    // Update equipment status if any anomalies
    const hasAnomalies = data.some(r => r.is_anomaly);
    if (hasAnomalies) {
        // Refresh stats periodically (debounced)
        debouncedRefreshStats();
    }
}

/**
 * Update the always-visible live sensor readout below each Digital Twin node.
 * Shows the 2 most important metrics (e.g. temperature + vibration) compactly,
 * positioned below the card so it never overlaps neighbouring boxes.
 */
function updateTwinSensors(readings) {
    const byEquipment = {};
    readings.forEach(r => {
        if (!byEquipment[r.equipment_id]) byEquipment[r.equipment_id] = [];
        byEquipment[r.equipment_id].push(r);
    });

    const priority = ['temperature', 'vibration', 'pressure', 'flow_rate', 'casting_speed',
                      'oil_temperature', 'hydraulic_pressure', 'load_weight', 'gas_flow',
                      'dust_level', 'water_temp_in', 'motor_current', 'electrode_current'];

    const iconFor = (metric) => (
          metric === 'temperature'       ? '🌡️'
        : metric === 'vibration'         ? '📳'
        : metric === 'fan_vibration'     ? '📳'
        : metric === 'pressure'          ? '🔵'
        : metric === 'flow_rate'         ? '💧'
        : metric === 'casting_speed'     ? '⚡'
        : metric === 'load_weight'       ? '🏋️'
        : metric === 'dust_level'        ? '🌫️'
        : metric === 'oil_temperature'   ? '🛢️'
        : metric === 'hydraulic_pressure'? '🔩'
        : metric === 'gas_flow'          ? '💨'
        : '⚡');

    Object.keys(byEquipment).forEach(eqId => {
        const readout = document.getElementById(`dt-sensor-${eqId}`);
        if (!readout) return;

        const metrics = byEquipment[eqId];

        // Deduplicate by metric (keep latest occurrence first)
        const byMetric = {};
        metrics.forEach(m => { if (!(m.metric in byMetric)) byMetric[m.metric] = m; });

        // Always prefer a TEMPERATURE-type and a VIBRATION-type reading so every
        // card consistently shows "°C  ·  mm/s".
        const tempKeys = ['temperature', 'oil_temperature', 'water_temp_in', 'hoist_motor_temp'];
        const vibKeys  = ['vibration', 'fan_vibration'];

        const pick = (keys) => { for (const k of keys) if (byMetric[k]) return byMetric[k]; return null; };
        const chosen = [];
        const tempM = pick(tempKeys);
        const vibM  = pick(vibKeys);
        if (tempM) chosen.push(tempM);
        if (vibM)  chosen.push(vibM);

        // Fallback: if a card has neither, show its top 2 metrics by priority.
        if (chosen.length < 2) {
            const sorted = Object.values(byMetric).sort((a, b) => {
                const ia = priority.indexOf(a.metric);
                const ib = priority.indexOf(b.metric);
                return (ia === -1 ? 99 : ia) - (ib === -1 ? 99 : ib);
            });
            for (const m of sorted) {
                if (chosen.length >= 2) break;
                if (!chosen.includes(m)) chosen.push(m);
            }
        }

        const cells = chosen.slice(0, 2).map(m => {
            const cls = m.is_anomaly ? 'anomaly' : (m.early_warning ? 'warn' : '');
            return `<span class="dt-ro-cell ${cls}">${iconFor(m.metric)} ${m.value}<small>${m.unit || ''}</small></span>`;
        }).join('');

        if (cells) {
            readout.innerHTML = cells;
            readout.classList.add('has-data');
        }
    });
}

