/**
 * Load the available demo scenarios and render the trigger cards.
 */
async function loadScenarios() {
    const grid = document.getElementById('scenario-grid');
    if (!grid) return;

    try {
        const result = await apiRequest('/api/scenarios/list');
        const scenarios = result.data || [];

        const sevEmoji = { critical: '🔴', high: '🟠', medium: '🟡', low: '🟢' };

        grid.innerHTML = scenarios.map(s => `
            <div class="scenario-card" id="scenario-${s.id}"
                 onclick="triggerScenario('${s.id}', this)">
                <div class="scenario-card-name">${sevEmoji[s.severity] || '⚪'} ${s.name}</div>
                <div class="scenario-card-desc">${s.description}</div>
                <div class="scenario-card-eq">${s.equipment_id} · ${s.severity.toUpperCase()}</div>
            </div>
        `).join('');
    } catch (error) {
        console.error('Failed to load scenarios:', error);
        grid.innerHTML = '<div style="color: var(--text-muted); font-size: 12px;">Scenarios unavailable.</div>';
    }
}

/**
 * Trigger a demo scenario. Injects degradation + alerts on the backend
 * and reflects the change live on the dashboard.
 */
async function triggerScenario(scenarioId, cardEl) {
    if (cardEl) {
        cardEl.classList.add('triggered');
        cardEl.style.pointerEvents = 'none';
    }

    try {
        const result = await apiRequest('/api/scenarios/trigger', {
            method: 'POST',
            body: JSON.stringify({ scenario_id: scenarioId }),
        });

        if (result.success) {
            showToast(result.data.message || 'Scenario triggered', 'warning');
            // Refresh dashboard state so status + health rings reflect the change.
            await Promise.all([loadDashboardStats(), loadEquipment()]);
        } else {
            showToast(result.error || 'Failed to trigger scenario', 'error');
            if (cardEl) {
                cardEl.classList.remove('triggered');
                cardEl.style.pointerEvents = '';
            }
        }
    } catch (error) {
        console.error('Failed to trigger scenario:', error);
        showToast('Failed to trigger scenario', 'error');
        if (cardEl) {
            cardEl.classList.remove('triggered');
            cardEl.style.pointerEvents = '';
        }
    }
}

/**
 * Reset all equipment back to operational and clear scenario state.
 */
async function resetScenarios() {
    try {
        const result = await apiRequest('/api/scenarios/reset', { method: 'POST' });
        if (result.success) {
            showToast('All equipment reset to operational', 'success');
            document.querySelectorAll('.scenario-card.triggered').forEach(card => {
                card.classList.remove('triggered');
                card.style.pointerEvents = '';
            });
            await Promise.all([loadDashboardStats(), loadEquipment()]);
        }
    } catch (error) {
        console.error('Failed to reset scenarios:', error);
        showToast('Failed to reset scenarios', 'error');
    }
}
