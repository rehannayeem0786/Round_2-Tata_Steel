/**
 * Dashboard module — Equipment health monitoring,
 * stats cards, and real-time sensor data visualization.
 */

let dashboardData = {
    stats: null,
    equipment: [],
};

/**
 * Initialize the dashboard.
 */
async function initDashboard() {
    await Promise.all([
        loadDashboardStats(),
        loadEquipment(),
        loadScenarios(),
    ]);

    // Register for real-time sensor updates
    onWsMessage('sensor_update', handleSensorUpdate);

    // Register for action log updates
    onWsMessage('action_log', () => {
        showToast('New ERP/SCADA action generated by AI Agent', 'info');
        loadDashboardStats();
        loadEquipment();
    });
    
    onWsMessage('action_log_update', () => {
        loadDashboardStats();
        loadEquipment();
    });
}

/**
 * Load dashboard statistics.
 */
async function loadDashboardStats() {
    try {
        const role = window.currentUserRole || 'engineer';
        if (role === 'manager') {
            const [statsRes, costRes, actionsRes] = await Promise.all([
                apiRequest('/api/dashboard/stats'),
                apiRequest('/api/analytics/cost-summary'),
                apiRequest('/api/dashboard/actions?limit=10')
            ]);
            dashboardData.stats = statsRes.data;
            const pendingActionsCount = (actionsRes.data || []).filter(a => a.status === 'pending').length;
            renderManagerStatsCards(statsRes.data, costRes.data, pendingActionsCount);
        } else {
            const result = await apiRequest('/api/dashboard/stats');
            dashboardData.stats = result.data;
            renderStatsCards(result.data);
        }
    } catch (error) {
        console.error('Failed to load stats:', error);
    }
}

/**
 * Load equipment list.
 */
async function loadEquipment() {
    try {
        const result = await apiRequest('/api/dashboard/equipment');
        dashboardData.equipment = result.data;
        renderEquipmentGrid(result.data);
        renderDigitalTwin(result.data);
        renderRoleSpecificSection();
    } catch (error) {
        console.error('Failed to load equipment:', error);
    }
}

