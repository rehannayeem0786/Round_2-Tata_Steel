/**
 * Render digital twin with SVG flow lines.
 */
function renderDigitalTwin(equipment) {
    const container = document.getElementById('digital-twin-container');
    if (!container) return;

    // Define stage info with layout positioning (x, y)
    // and matching equipment ID
    const nodesDef = [
        // Main Production Line (y = 65)
        { id: 'BF-001', name: 'Blast Furnace #1', icon: '🔥', x: 40, y: 65, next: 'BOF-001' },
        { id: 'BOF-001', name: 'BOF Converter #1', icon: '⚗️', x: 200, y: 65, next: 'LF-001' },
        { id: 'LF-001', name: 'Ladle Furnace #1', icon: '⚡', x: 360, y: 65, next: 'CCM-001' },
        { id: 'CCM-001', name: 'Continuous Caster', icon: '🏭', x: 520, y: 65, next: 'HRM-001' },
        { id: 'HRM-001', name: 'Hot Rolling Mill', icon: '⚙️', x: 680, y: 65, next: 'CRM-001' },
        { id: 'CRM-001', name: 'Cold Rolling Mill', icon: '🗜️', x: 840, y: 65, next: null },

        // Auxiliaries / Utilities (y = 220)
        { id: 'CR-001', name: 'Charging Crane', icon: '🏗️', x: 120, y: 220, target: 'BOF-001' },
        { id: 'HP-001', name: 'Hydraulic Press', icon: '🧲', x: 280, y: 220, target: 'LF-001' },
        { id: 'CT-001', name: 'Cooling Tower #3', icon: '💧', x: 440, y: 220, target: 'CCM-001' },
        { id: 'GR-001', name: 'Gas Recovery', icon: '🌬️', x: 600, y: 220, target: 'HRM-001' }
    ];

    // Build map of equipment status
    const eqStatusMap = {};
    equipment.forEach(eq => {
        eqStatusMap[eq.id] = eq.status;
    });

    // Generate HTML for nodes
    let htmlNodesContent = '';
    nodesDef.forEach(node => {
        const status = eqStatusMap[node.id] || 'operational';
        htmlNodesContent += `
            <div class="dt-node-card status-${status}" style="left: ${node.x}px; top: ${node.y}px;" onclick="askAboutEquipment('${node.id}', '${node.name}')">
                <div class="dt-node-card-icon">${node.icon}</div>
                <div class="dt-node-card-name">${node.name}</div>
                <div class="dt-node-card-status ${status}">${status.toUpperCase()}</div>
                <div class="dt-node-readout" id="dt-sensor-${node.id}"></div>
            </div>
        `;
    });

    // Generate SVG path connectors.
    // Each connection draws two stacked paths:
    //   1. the colored status line (subtle, status-coloured dashes)
    //   2. a bright white "pulse" overlay that flows along the path (material flow)
    let svgPathsContent = '';
    nodesDef.forEach(node => {
        const status = eqStatusMap[node.id] || 'operational';
        const flowClass = `flow-${status}`;

        if (node.next) {
            // Find target node coordinates
            const nextNode = nodesDef.find(n => n.id === node.next);
            if (nextNode) {
                // Horizontal path between centers
                const x1 = node.x + 110;
                const y1 = node.y + 37;
                const x2 = nextNode.x;
                const y2 = nextNode.y + 37;
                const d = `M ${x1} ${y1} L ${x2} ${y2}`;
                svgPathsContent += `<path d="${d}" class="dt-flow-path ${flowClass}" />`;
                svgPathsContent += `<path d="${d}" class="dt-flow-pulse ${flowClass}" />`;
            }
        }

        if (node.target) {
            // Curved path from auxiliary to target
            const targetNode = nodesDef.find(n => n.id === node.target);
            if (targetNode) {
                const x1 = node.x + 55;
                const y1 = node.y;
                const x2 = targetNode.x + 55;
                const y2 = targetNode.y + 75;
                const cpY1 = y1 - 40;
                const cpY2 = y2 + 40;
                const d = `M ${x1} ${y1} C ${x1} ${cpY1}, ${x2} ${cpY2}, ${x2} ${y2}`;
                svgPathsContent += `<path d="${d}" class="dt-flow-path ${flowClass}" />`;
                svgPathsContent += `<path d="${d}" class="dt-flow-pulse ${flowClass}" />`;
            }
        }
    });

    // Inject layout wrapper
    container.innerHTML = `
        <div class="dt-plant-wrapper">
            <!-- HTML Nodes Layer -->
            <div class="dt-html-nodes">
                ${htmlNodesContent}
            </div>
            <!-- SVG Connectors Layer -->
            <svg class="dt-svg-overlay" width="100%" height="100%" viewBox="0 0 1000 350">
                ${svgPathsContent}
            </svg>
        </div>
    `;
}

