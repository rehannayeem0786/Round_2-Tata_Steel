/**
 * Render an animated circular health-score ring for an equipment card.
 * Expects the `health` object returned by the dashboard API
 * ({ score, grade, label, factors }).
 */
function renderHealthRing(health) {
    if (!health || typeof health.score !== 'number') return '';

    const score = Math.max(0, Math.min(100, health.score));
    const grade = (health.grade || 'C').toLowerCase();
    const label = health.label || 'Fair';

    // Geometry: 44x44 viewport, centre 22,22, radius 18 (stroke-width 4).
    const radius = 18;
    const circumference = 2 * Math.PI * radius;
    const offset = circumference * (1 - score / 100);

    const colorMap = {
        a: '#10b981',
        b: '#3b82f6',
        c: '#f59e0b',
        d: '#ef4444',
        f: '#ef4444',
    };
    const stroke = colorMap[grade] || '#f59e0b';

    return `
        <div class="health-ring-container">
            <div class="health-ring">
                <svg viewBox="0 0 44 44">
                    <circle class="health-ring-bg" cx="22" cy="22" r="${radius}"></circle>
                    <circle class="health-ring-fill" cx="22" cy="22" r="${radius}"
                        stroke="${stroke}"
                        stroke-dasharray="${circumference.toFixed(2)}"
                        stroke-dashoffset="${offset.toFixed(2)}"></circle>
                </svg>
                <span class="health-ring-value" style="color:${stroke}">${score}</span>
            </div>
            <div>
                <span class="health-ring-label">Health: ${label}</span>
                <span class="health-ring-grade grade-${grade}">${(health.grade || 'C')}</span>
            </div>
        </div>
    `;
}

